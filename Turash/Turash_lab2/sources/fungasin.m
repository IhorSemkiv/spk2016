function f = fungasin(x)
% Тестова функція з синусами
% Глобальний мінімум знаходиться в точці 
% x = [9.038991604817884 8.6681889577870734]' с fx = [-18.554721077382709]'
% 0 <= x <= 10
% Вхідний параметр: x - двовимірний вектор змінних
% Вихідний параметр: f - значення функції
f = x(1).*sin(4*x(1)) + 1.1*x(2).*sin(2*x(2));
